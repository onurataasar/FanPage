package com.example.fanpageproject;

public class Post {
    private String Post;

    public Post() {
        this.Post = Post;
    }

    public String getPost() {
        return Post;
    }

    public void setPost(String post) {
        Post = post;
    }

    public String toString() {
        return "\n" + this.Post+ " " + "\n";
    }
}
